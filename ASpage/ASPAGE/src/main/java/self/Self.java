package self;

public class Self {
	private int selfID;
	private String selfTitle;
	private String userID;
	private String selfDate;
	private String selfContent;
	private int selfAvailable;
	
	public int getSelfID() {
		return selfID;
	}
	public void setSelfID(int selfID) {
		this.selfID = selfID;
	}
	public String getSelfTitle() {
		return selfTitle;
	}
	public void setSelfTitle(String selfTitle) {
		this.selfTitle = selfTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getSelfDate() {
		return selfDate;
	}
	public void setSelfDate(String selfDate) {
		this.selfDate = selfDate;
	}
	public String getSelfContent() {
		return selfContent;
	}
	public void setSelfContent(String selfContent) {
		this.selfContent = selfContent;
	}
	public int getSelfAvailable() {
		return selfAvailable;
	}
	public void setSelfAvailable(int selfAvailable) {
		this.selfAvailable = selfAvailable;
	}
	
	
}
